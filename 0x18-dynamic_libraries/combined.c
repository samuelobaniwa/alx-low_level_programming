#include <stdio.h>
/**
 * add - add two integers
 * @a: first integer
 * @b: second integer
 * Return: Addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two integers
 * @a: first integer
 * @b: second integer
 * Return: subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two integers
 * @a: first integer
 * @b: second integer
 * Return: multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - modulle two integers
 * @a: first integer
 * @b: second integer
 * Return: modullus
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * div - divide two integers
 * @a: first integers
 * @b: second integer
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}
